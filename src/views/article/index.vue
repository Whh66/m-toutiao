<template>
  <div class="article-container">
    <!-- 导航栏 -->
    <van-nav-bar class="page-nav-bar" title="黑马头条">
      <van-icon slot="left" name="arrow-left" @click="$router.back()" />
    </van-nav-bar>
    <!-- /导航栏 -->

    <div class="main-wrap">
      <!-- 加载中 -->
      <div v-if="loading" class="loading-wrap">
        <van-loading color="#3296fa" vertical>加载中</van-loading>
      </div>
      <!-- /加载中 -->

      <!-- 加载完成-文章详情 -->
      <div v-else-if="article.title" class="article-detail">
        <!-- 文章标题 -->
        <h1 class="article-title">{{article.title}}</h1>
        <!-- /文章标题 -->

        <!-- 用户信息 -->
        <van-cell class="user-info" center :border="false">
          <van-image class="avatar" slot="icon" round fit="cover" :src="article.aut_photo" />
          <div slot="title" class="user-name">{{article.aut_name}}</div>
          <div slot="label" class="publish-date">{{article.pubdate | relativeTime}}</div>
          <!-- <follow-user :isFollowed="article.is_followed" :userId="article.aut_id" @update-is_followed="article.is_followed=$event" /> -->
          <!-- 当我们传递给子组件得数据既要使用换药修改时。有一个简写方式:   v-model 它就相当于如下默认方式的简写
            :value="article.is_followed"
            @input="article.is_followed=$event"
           -->
          <follow-user v-model="article.is_followed" :userId="article.aut_id" />
          <!--
          <van-button class="follow-btn" round size="small" v-if="article.is_followed" @click="onFollow" :loading="followLoading">已关注</van-button>
          <van-button class="follow-btn" type="info" color="#3296fa" round size="small" icon="plus" v-else @click="onFollow" :loading="followLoading">关注</van-button>
         -->
