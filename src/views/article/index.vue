      <div v-else class="error-wrap">
        <van-icon name="failure" />
        <p class="text">内容加载失败！</p>
        <van-button class="retry-btn" @click="loadArticle">点击重试</van-button>
      </div>
      <!-- /加载失败：其它未知错误（例如网络原因或服务端异常） -->
    </div>

    <!-- 评论回复弹出层 -->
    <van-popup v-model="isReplyShow" position="bottom" style="height:100%;">
      <comment-reply :comment="currentComment" @close="isReplyShow=false" v-if="isReplyShow" />
    </van-popup>
    <!-- /评论回复弹出层 -->
  </div>
</template>

<script>
import { getArticleById } from '@/api/article'
import { ImagePreview } from 'vant'
import FollowUser from '@/components/follow-user'
import CollectArticle from '@/components/collect-article'
import LikeArticle from '@/components/like-article'
import CommentList from './components/comment-list.vue'
import CommentPost from './components/comment-post.vue'
import CommentReply from './components/comment-reply.vue'

// ImagePreview({
//   images: [
//     'https://img01.yzcdn.cn/vant/apple-1.jpg',
//     'https://img01.yzcdn.cn/vant/apple-2.jpg',
//   ],
//   startPosition: 0,
//   onClose() {
//     this.$toast('关闭');
//   },
// });

export default {
  name: 'ArticleIndex',
  components: {
    FollowUser,
    CollectArticle,
    LikeArticle,
    CommentList,
    CommentPost,
    CommentReply
  },
  // 给所有组件提供数据
  provide: function () {
    return {
      articleId: this.articleId
    }
  },
  props: {
    articleId: {
      type: [Number, String, Object],
      required: true
    }
  },
  data () {
    return {
      article: {}, // 文章详情
      loading: true, // 加载中的loading状态
      errStatus: 0, // 失败的状态码
      totalCommentCount: 0, // 展示文章总评论
      isPostShow: false,
      commentList: [], // 评论列表
      isReplyShow: false, // 控制评论回复弹框
      currentComment: {} // 当前点击的评论项
    }
  },
  computed: {},
  watch: {},
  created () {
    this.loadArticle()
  },
  mounted () { },
  methods: {
    async loadArticle () {
      this.loading = true
      try {
        const { data } = await getArticleById(this.articleId.toString())

        // 随机制造一些错误, 来模拟失败效果
        // if (Math.random() > 0.5) {
        //   JSON.parse('dssssss')
        // }

        this.article = data.data

        // 初始化图片点击预览
        // console.log(this.$refs['article-content']);
        setTimeout(() => {
          // console.log(this.$refs['article-content']);
          this.previewImage()
        }, 0)

        // 请求成功，关闭loading
        // this.loading = false
      } catch (err) {
        // this.loading = false
        if (err.response && err.response.status === 404) {
          this.errStatus = 404
        }
      }
      // 无论成功还是失败，都得关闭loading
      this.loading = false
    },
    previewImage () {
      // 得到所有img节点
      const articleContent = this.$refs['article-content']
      const imgs = articleContent.querySelectorAll('img')
      const images = []
      imgs.forEach((img, index) => {
        images.push(img.src)
        img.onclick = () => {
          ImagePreview({
            images,
            startPosition: index
          })
        }
      })
      // console.log(images);
    },
    onPostSuccess (data) {
      this.isPostShow = false
      this.commentList.unshift(data.new_obj)
    },
    onReplyClick (comment) {
      this.isReplyShow = true
      this.currentComment = comment
    }
  }
}
</script>

